def processed_file(file_id):
    matches = sorted(p for p in PROCESSED.glob('*.json') if p.name.startswith(file_id))
    if not matches:
        raise FileNotFoundError(file_id)
    return matches[0]


def word_count(path):
    data = json.loads(path.read_text(encoding='utf-8'))
    text = ' '.join(item.get('text', '') for item in data.get('all_content', []))
    return len(re.findall(r"[A-Za-z]+(?:[-'][A-Za-z]+)?", text))


def recommendation_for(file_id, meta):
    if file_id in BOOK_RECOMMENDATIONS:
        return BOOK_RECOMMENDATIONS[file_id]
    if file_id in EARLY_RECOMMENDATIONS:
        return EARLY_RECOMMENDATIONS[file_id]
    return meta.get('intro', '')


def collect_core_quotes(book_ids):
    quotes_by_book = {}
    seen_by_book = {}
    ordered_ids = sorted(book_ids, key=len, reverse=True)
    if not QUOTES_DIR.exists():
        return quotes_by_book
    for path in sorted(QUOTES_DIR.glob('*.json')):
        data = json.loads(path.read_text(encoding='utf-8'))
        excerpts = data.get('excerpts', []) if isinstance(data, dict) else data
        if not isinstance(excerpts, list):
            continue
        for ex in excerpts:
            if not isinstance(ex, dict):
                continue
            source_file = ex.get('source_file') or ''
            book_id = next((bid for bid in ordered_ids if source_file.startswith(bid + '-')), '')
            text_zh = re.sub(r'\s+', ' ', (ex.get('text_zh') or ex.get('translation') or '').strip())
            if not book_id or not text_zh:
                continue
            seen = seen_by_book.setdefault(book_id, set())
            if text_zh in seen:
                continue
            seen.add(text_zh)
            quotes_by_book.setdefault(book_id, []).append({
                'text': text_zh,
                'score': float(ex.get('score') or 0),
                'semantic_score': float(ex.get('semantic_score') or 0),
            })
    result = {}
    for book_id, quotes in quotes_by_book.items():
        ranked = sorted(quotes, key=lambda item: (item['score'], item['semantic_score'], len(item['text'])), reverse=True)
        result[book_id] = [item['text'] for item in ranked[:20]]
    return result


def build_books():
    summary = json.loads(SUMMARY_FILE.read_text(encoding='utf-8'))
    summary_books = summary.get('books', [])
    core_quotes = collect_core_quotes([item['file_id'] for item in summary_books])
    books = []
    for item in summary_books:
        file_id = item['file_id']
        meta = BOOK_META.get(file_id, {})
        if file_id.startswith('ES-'):
            meta = {**EARLY_DEFAULTS, **meta}
        name_zh = meta.get('name_zh') or EARLY_ZH.get(file_id) or item['title']
        core_concepts = meta.get('core_concepts', [])
        books.append({
            'id': file_id,
            'name_zh': name_zh,
            'name_en': item['title'],
            'word_count': word_count(processed_file(file_id)),
            'sessions_count': item.get('sessions_count', 0),
            'intro': recommendation_for(file_id, meta),
            'toc': meta.get('toc', []),
            'core_concepts': core_concepts,
            'core_quotes': core_quotes.get(file_id, []),
        })
    return {'books': books}


def main():
    data = build_books()
    BOOK_JSON.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding='utf-8')
    BOOK_JS.write_text(
        'window.SETH_BOOKS_DATA = ' + json.dumps(data, ensure_ascii=False, indent=2) + ';\n',
        encoding='utf-8',
    )
    print(f'OK: wrote {BOOK_JSON}')
    print(f'OK: wrote {BOOK_JS}')
    print(f'books: {len(data["books"])}')


if __name__ == '__main__':
    main()
